def test_atoms_get_moments_of_inertia(
    azulene_gasphase,
    azulene_pbc,
    azulene_moments_of_inertia,
    azulene_pbc_moments_of_inertia,
):
    moments = azulene_gasphase.get_moments_of_inertia()
    assert moments == pytest.approx(azulene_moments_of_inertia, rel=1e-8)

    # this should raise a user warning
    with pytest.warns(UserWarning):
        moments_pbc = azulene_pbc.get_moments_of_inertia()
        assert moments_pbc == pytest.approx(
            azulene_pbc_moments_of_inertia, rel=1e-8
        )
